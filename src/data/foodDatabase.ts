/** 按类别获取食物列表 */
export function getFoodsByCategory(category: FoodDatabaseItem['category']): FoodDatabaseItem[] {
  return foodDatabase.filter(f => f.category === category);
}

/** 搜索食物（按名称模糊匹配） */
export function searchFoods(keyword: string): FoodDatabaseItem[] {
  if (!keyword.trim()) return [];
  const lower = keyword.toLowerCase();
  return foodDatabase.filter(f => f.name.toLowerCase().includes(lower));
}

/** 根据 ID 获取食物 */
export function getFoodById(id: string): FoodDatabaseItem | undefined {
  return foodDatabase.find(f => f.id === id);
}

